func TestBasic(t *testing.T) {
	display, err := getLCD(t, basicTest)

	if err != nil {
		t.Fatal(err)
	}
	defer shutdown(t)
	s := display.String()
	t.Log(s)
	if len(s) == 0 {
		t.Error("display.String()")
	}

	_, err = display.WriteString("1234567890")
	if err != nil {
		t.Error(err)
	}
	err = display.MoveTo(2, 2)
	if err != nil {
		t.Error(err)
	}
	_, err = display.WriteString("2345678901")
	if err != nil {
		t.Error(err)
	}
	rows := display.Rows()
	if rows != testRows {
		t.Errorf("display.Rows() expected %d, received %d", testRows, rows)
	}
	cols := display.Cols()
	if cols != testCols {
		t.Errorf("display.Cols() expected %d, received %d", testCols, cols)
	}
	if liveDevice {
		time.Sleep(5 * time.Second)
	}
	err = display.Halt()
	if err != nil {
		t.Error(err)
	}
}

